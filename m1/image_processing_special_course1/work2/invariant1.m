for i = 1:16
    x1 = xx(1,:,i);
    x2 = xx(2,:,i);
    x3 = xx(3,:,i);
    x4 = xx(4,:,i);
    xc = crosspoint(x1,x2,x3,x4);
    invariants(i) = det([x1;xc])/det([x1;x2]);
end;

for i = 1:16
    for j = i+1 :16
        wa = abs(invariants(i)) + abs(invariants(j));
        sa = abs(invariants(i) - invariants(j));
        if sa <= wa*0.001
            ans = [i j];
            disp(ans);
        end;
    end;
end;








